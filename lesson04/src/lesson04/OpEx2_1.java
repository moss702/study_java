package lesson04;

public class OpEx2_1 {
	public static void main(String[] args) {
		
		int a = 5;
		System.out.println("+a = " + +a);
		//양수에게 +를 붙이는건 별 의미없다. 생략가능
		System.out.println("-a = " + -a);
		//부정부호. 반대로 만든다.
		
		a = - 5;
		System.out.println("+a = " + +a);
		System.out.println("-a = " + -a);
	}
}
